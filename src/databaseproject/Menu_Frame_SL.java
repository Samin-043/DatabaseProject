    public void sales_part()
    {
        try
        {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            Connection connection = DriverManager.getConnection("jdbc:sqlserver://localhost:1433;databaseName=WarehouseManagementSystem;selectMethod=cursor", "sa", "123467");
            Statement statement = connection.createStatement();

            String query = "select username from log_in_sales";
            
            PreparedStatement ps = connection.prepareStatement(query);
            ResultSet resultSet = ps.executeQuery();
            
            if(resultSet.next())
            {
                String user_name = resultSet.getString("username");
                jMenu_Admin_logout.setText(user_name);
            }
            
            
        }
        catch (Exception e) {

            e.printStackTrace();

        }
    }
    
