    private void s_editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_s_editButtonActionPerformed
        // TODO add your handling code here:
         try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            String url = "jdbc:sqlserver://localhost:1433;databaseName=WarehouseManagementSystem;user=sa;password=123467";
            Connection con = DriverManager.getConnection(url);
            int row = Display_Staff.getSelectedRow();
            String value = (Display_Staff.getModel().getValueAt(row, 0)).toString();
            String query = "update staff set name=?,staff_address=?,phone_no=?,email=?,staff_title=?,customer_id=? where staff_id="+value;
            PreparedStatement pst = con.prepareStatement(query);
            pst.setString(1, s_name.getText());
            pst.setString(2, s_address.getText());
            pst.setString(3, s_phone.getText());
            pst.setString(4, s_email.getText());
            pst.setString(5, s_title.getText());
            pst.setString(6, s_customer_id.getText());
            
            pst.executeUpdate();
            DefaultTableModel model = (DefaultTableModel)Display_Staff.getModel();
            model.setRowCount(0);
            show_staff();
            JOptionPane.showMessageDialog(null, "Updated Successfully!");
        }catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_s_editButtonActionPerformed

