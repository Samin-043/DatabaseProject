    public ArrayList<StaffFrame> staffsList(){
        ArrayList<StaffFrame> staffsList = new ArrayList<>();
         try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            String url = "jdbc:sqlserver://localhost:1433;databaseName=WarehouseManagementSystem;user=sa;password=123467";
            Connection con = DriverManager.getConnection(url);
            String query1 = "Select * from staff";
             Statement st = con.createStatement();
             ResultSet rs = st.executeQuery(query1);
             
             StaffFrame staffframe;
             while(rs.next()){
                 staffframe = new StaffFrame(rs.getInt("staff_id"),rs.getInt("customer_id"),rs.getString("name"),rs.getString("staff_address"),rs.getString("phone_no"),rs.getString("email"),rs.getString("staff_title"));
                 staffsList.add(staffframe);
             }
         }catch (Exception e) {
            e.printStackTrace();
        }
         return staffsList;
    }
    
    public void show_staff(){
        ArrayList<StaffFrame> list = staffsList();
        DefaultTableModel model = (DefaultTableModel)Display_Staff.getModel();
        Object[] row = new Object[7];
        for(int i=0;i<list.size();i++){
            row[0] = list.get(i).getStaff_id();
            row[1] = list.get(i).getCustomer_id();
            row[2] = list.get(i).getName();
            row[3] = list.get(i).getStaff_address();
            row[4] = list.get(i).getPhone_no();
            row[5] = list.get(i).getEmail();
            row[6] = list.get(i).getStaff_title();
            model.addRow(row);
        }
    }

